                <p>
                    Antes de salir de vacaciones es importante tener el vehículo en perfectas condiciones. 
                    Aquí te brindamos siete recomendaciones para evitar posibles averías y disfrutar de tu 
                    viaje sin inconvenientes en la ruta.
                </p>

                <h4>Siete consejos para un viaje óptimo</h2>

                <ol>
                    <li>
                        <p>
                            <strong>Revisa tus neumáticos.</strong> Asegúrate que tengan la presión 
                            indicada por el fabricante y no te olvides de chequear el estado del de repuesto.
                        </p>
                    </li>

                    <li>
                        <p>
                            <strong>Cuida el desgaste de tu vehículo.</strong>  Si vas a realizar un 
                            viaje largo, lleva un litro de aceite extra para el motor, en especial, 
                            si tu auto emplea uno sintético o algún grosor poco frecuente de encontrar.
                        </p>
                    </li>

                    <li>
                        <p>
                            <strong> Realiza un control de frenos. </strong> En época de 
                            vacaciones, encontrarás revisiones a precios accesibles en la 
                            mayoría de los servicentros. Mide además los gases de escape, la 
                            dirección, la suspensión y la firmeza del tren delantero.
                        </p>
                    </li>

                    <li>
                        <p>
                            <strong>Asegúrate que el limpiaparabrisas y las plumillas se encuentren en 
                                buenas condiciones.</strong> La lluvia puede aparecer en cualquier momento 
                                y la visibilidad al volante se vuelve algo imprescindible.
                        </p>
                    </li>

                    <li>
                        <p>
                            <strong>Chequea los elementos de seguridad</strong> Revisa que la carga de tu 
                            extintor de incendios esté completa, controla el estado de todas las luces y 
                            lleva los triángulos que exige la ley. Prepara un botiquín completo de primeros 
                            auxilios y no te olvides de la llave de ruedas y la gata. Recuerda también tener 
                            a mano la póliza de tu compañía de Seguros de Autos.
                        </p>
                    </li>

                    <li>
                        <p>
                            <strong>Evita agregar carga en el techo del vehículo. </strong> Si no cuentas 
                            con una cajuela apropiada para ello, colocar objetos en el techo puede generar 
                            inestabilidad en la conducción: demasiado peso puede elevar el centro de gravedad 
                            y modificar el eje aerodinámico del auto.
                        </p>
                    </li>

                    <li>
                        <p>
                            <strong>Prepárate a ti mismo.</strong> Si vas a viajar durante varias horas, 
                            conduce con ropa cómoda, lleva una botella de agua para mantenerte hidratado y 
                            algunos plátanos para evitar calambres. En caso de conducir de noche, abre un 
                            poco la ventana y sintoniza buena música para mantenerte despierto. En caso de 
                            sufrir un accidente, no dudes en solicitar ayuda la asistencia de tu compañía 
                            de Seguros Automotrices.
                        </p>
                    </li>
                </ol>
