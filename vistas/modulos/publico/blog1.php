                <h4>¿Se puede averiar mi coche si el nivel de aceite es muy bajo? ¿Y si es muy alto?</h4>

                <p>
                    Es importante que el nivel de aceite sea el correcto. No debe ser ni muy alto, ni muy bajo. 
                    Con un nivel muy bajo el motor no se lubricará correctamente y aumentará la fricción de sus 
                    piezas internas, que se desgastarán de forma prematura y acelerada. En casos extremos, el 
                    calor generado puede ser tan alto que el motor puede llegar a "gripar", quedando completamente 
                    inservible. No obstante, si el nivel de aceite es muy alto también se pueden producir daños en 
                    la mecánica. Para empezar, no podrán hacernos la prueba de gases en la ITV, pero ese es el menor 
                    de nuestros problemas.
                </p>

                <p>
                    Se puede crear una sobrepresión en el sistema de lubricación, provocando averías en el propio 
                    sistema de lubricación y sometiendo a presiones innecesarias a componentes como retenes del motor. 
                    Además, el exceso de aceite será quemado, provocando daños en el catalizador y sistema de escape - 
                    especialmente en coches equipados con filtro de partículas. Si el nivel de aceite es excesivo, haz 
                    que un taller extraiga el aceite sobrante con una chupona para evitar daños mayores.
                </p>

                <h4>Mi coche no tiene varilla, ¿cómo reviso el nivel de aceite?</h4>

                <p>
                    Si tu coche no tiene varilla, también puedes revisar el nivel de aceite sin problema alguno. 
                    En algunos coches modernos, la varilla tradicional ha sido reemplazada por un sensor de nivel 
                    de aceite, conectado a la electrónica del coche. En ese caso, podrás acceder a la comprobación 
                    de nivel de aceite a través de su ordenador de a bordo, o a través de su sistema de infotainment. 
                    En este último caso, suele estar accesible en la sección de "servicio" o en la sección de "estado 
                    del coche". BMW fue una de las primeras marcas en eliminar la varilla del aceite en algunos de 
                    sus coches
                </p>

