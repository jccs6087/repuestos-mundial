            <div class="blog-descripcion">
                <p>
                    El aceite es la sangre de cualquier motor, ya tengas un coche diésel, 
                    gasolina o híbrido. Este lubricante se encarga de que las piezas de metal 
                    que componen el motor trabajen sin un excesivo nivel de fricción. La enorme 
                    velocidad a la que se mueven los pistones del motor genera tanto calor y 
                    rozamiento que sin una correcta lubricación, el motor no funcionaría correctamente 
                    y se estropearía rápidamente. Por ello es vital que sepas revisar el nivel de aceite del 
                    motor, una comprobación que recomendamos hacer de forma periódica. Este artículo es la 
                    guía definitiva para saber cómo revisar el nivel de aceite de tu coche.
                </p>

                <h3>Por qué es necesario revisar el nivel de aceite del coche</h3>

                <p>
                    Desde que el motor de combustión interna existe, siempre ha empleado aceite como 
                    lubricante. Aunque sus tecnologías y materiales hayan evolucionado enormemente, 
                    este principio de funcionamiento se mantiene inalterado desde finales del siglo XIX. 
                    Aunque el sistema de lubricación es un sistema cerrado, es inevitable que todos los motores 
                    consuman algo de aceite, aunque sea un consumo ínfimo. Este consumo puede ser mayor en motores 
                    de alto rendimiento o motores con años. Con todo, muchos fabricantes admiten que un consumo de 
                    hasta un litro por cada 1.000 km no es indicativo de una avería  
                </p>

                <h3>Revisando el nivel de aceite del coche, paso a paso en detalle</h3>

                <p>
                    El primer paso para comprobar el nivel de aceite del coche es estacionar en una superficie 
                    llana. Aparcar en cuestas o con el coche inclinado hará que nuestra lectura sea errónea. 
                    Ahora bien, ¿debo medirlo en frío o en caliente? Personalmente lo he medido siempre en frío, 
                    algo que muchos fabricantes de coches consideran que es idóneo, ya que el aceite ha escurrido 
                    completamente y se encuentra acumulada en el cárter.
                </p>

                <p>
                    Ahora bien, otros fabricantes indican en sus manuales que debemos medir el nivel de aceite 
                    unos minutos después de apagar el motor. El motor debe haber alcanzado su temperatura de 
                    servicio, asegurando de esta manera que el aceite se ha fluidificado y caído al cárter en 
                    su totalidad. Si tienes dudas, consulta el manual de tu coche. Para medir el nivel de aceite 
                    debes extraer la varilla de medición. Suele tener una arandela de plástico de color amarillo 
                    o naranja, y es metálica. Sácala y límpiala con un trapo, o un trozo de papel de cocina. A 
                    continuación, vuelve a introducirla hasta el final.
                </p>

                <p>
                    Al sacarla otra vez, fíjate en el extremo de la varilla. Verás unas muescas o agujeros, 
                    que indican tanto el nivel máximo, como el nivel mínimo de aceite. Si el aceite está muy 
                    nuevo o no está aún manchado, puede que tengas que poner la varilla al trasluz para apreciar 
                    correctamente el nivel. La medición de aceite debería estar entre el mínimo y el máximo. Si se aproxima al mínimo o está por debajo del mínimo, deberías rellenar el nivel de aceite. Deberás usar un aceite de especificaciones y viscosidad idénticas a las de tu coche - información que encontrarás en el manual del propietario.
                </p>

                <p>
                    Existen coches con sistemas de lubricación por cárter seco, aunque es un sistema poco común - 
                    es mucho más habitual en el mundillo de las motos. En este tipo de coches, para medir el nivel 
                    hemos de arrancar el motor durante un rato, dejando que la bomba de aceite recupere todo el 
                    aceite y lo acumule en el depósito. Este depósito, situado en el vano motor, tendrá una tapa y 
                    una pequeña varilla para medir el nivel. El procedimiento es idéntico que en coches con lubricación 
                    por cárter húmedo: saca la varilla, límpiala, introdúcela de nuevo y comprueba el nivel de 
                    aceite sacándola de nuevo.
                </p>

